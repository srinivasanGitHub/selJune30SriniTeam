package week1.day1.homework;

public class ValuesOfDataTypes {

	public static void main(String[] args) {
		int intValue = 5;
		float floatValue = 220.15f;
		double doubleValue= 55.0d;
		long longValue= 8744;
		char charValue='H';
		boolean boolValue = true;
		short shortValue = 45;
		byte byteValue = 8;
		
		System.out.println("intValue= " + intValue +"\n"+
				"floatValue= " + floatValue +"\n"+
				"doubleValue= " + doubleValue +"\n"+
				"longValue= " + longValue +"\n"+
				"charValue= " + charValue +"\n"+
				"boolValue= " + boolValue +"\n"+
				"shortValue= " + shortValue +"\n"+
				"byteValue= " + byteValue +"\n");
		

	}

}
